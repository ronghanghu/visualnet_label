                                <p align="left" style="font-size: 24px">
                                    <span class="midText">Due to the great demand of object detection, recognition, annotation and retrieval in multimedia content analysis and video surveillance, it is very important to explore automatic machine algorithm to replace human labor in cognizing various visual objects in images and videos. Unfortunately, current process of machine learning undergoes a distinctive different way as compared to human cognition mechanism. That is, according to the state-of-the-art statistical pattern recognition theory, computer can only process and model well-aligned images, which needs a great labor of image labeling. </span>
                                </p>
                                <p align="left" style="font-size: 24px">
                                    <span class="midText"> The effort of Visual NET is to provide an online environments to collect and share well-labeled image samples, currently focusing on multi-resolution human objects, for the purposes of various research objectives, such as human detection, facial analysis and recognition, comparison of human cognition with computer vision etc.. With complete AJAX technique, this system will considerably simplify and accelerate labeling landmarks on both images and videos, making data collection easier than ever before. You may experience it today!</span>
